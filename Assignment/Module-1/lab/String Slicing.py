# String slicing example
txt = "Python Programming"

# Various slicing examples
print("Original String:", txt)
print("From index 0 to 5:", txt[0:6])       
print("From index 7 onwards:", txt[7:])     
print("Up to index 6:", txt[:6])            
print("Substring between 0 and 10:", txt[0:10])  
print("Last 5 characters:", txt[-5:])       
print("Every 2nd character:", txt[::2])     
print("Reverse string:", txt[::-1])         
