# Initial list
my_list = [10, 20, 30]

print("Original List:", my_list)

# Using append() → adds element at the end
my_list.append(40)
print("After append(40):", my_list)

# Using insert() → inserts element at a specific position
my_list.insert(1, 15)  # insert 15 at index 1
print("After insert(1, 15):", my_list)
