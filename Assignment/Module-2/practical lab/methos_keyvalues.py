# Creating a dictionary
my_dict = {
    "name": "rutvi",
    "age": 20,
    "city": "rajkot",
    "email": "r@example.com"
}

print("Original Dictionary:", my_dict)

# Getting keys
keys = my_dict.keys()
print("Keys:", keys)

# Getting values
values = my_dict.values()
print("Values:", values)

# Converting to list (optional)
print("Keys as List:", list(keys))
print("Values as List:", list(values))
